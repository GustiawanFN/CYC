package co.id.bbw.myapplication.model_dummy;

public class ModelMusic {

    private String posterMusic;
    private String musicName;
    private String albumMusic;

    public String getPosterMusic() {
        return posterMusic;
    }

    public void setPosterMusic(String posterMusic) {
        this.posterMusic = posterMusic;
    }

    public String getMusicName() {
        return musicName;
    }

    public void setMusicName(String musicName) {
        this.musicName = musicName;
    }

    public String getAlbumMusic() {
        return albumMusic;
    }

    public void setAlbumMusic(String albumMusic) {
        this.albumMusic = albumMusic;
    }
}
